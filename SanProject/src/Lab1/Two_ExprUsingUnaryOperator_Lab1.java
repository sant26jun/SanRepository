package Lab1;


public class Two_ExprUsingUnaryOperator_Lab1 {
	
	public static void main(String args[])
	{	
		int r = 1;
		  System.out.println("r=: " + r++);
		  System.out.println("r=: " + r);
		  
		  int x = 2;
		  System.out.println("x=: " + x--);
		  System.out.println("x=: " + x);
		  
		  int y = 3;
		  System.out.println("y=: " + ++y);

		  int p = 6;
		  System.out.println("p=: " + --p);
		 }
		}